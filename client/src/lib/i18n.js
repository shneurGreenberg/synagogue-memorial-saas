function getInitialLng() {
  try {
    const stored = sessionStorage.getItem('boardLang');
    if (stored) {
      return stored;
    }
  } catch {
    /* ignore */
  }

  return (window.data && window.data.language) || 'ru';
}

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: getInitialLng(),
    fallbackLng: 'ru',
    interpolation: {
      escapeValue: false,
    },
  });

